
public class Parameter {
	public int sum(int a, int b)
	{
		int c= a+b;
		return c;
	}
	public double sub(double x, double y)
	{
		double z=x-y;
		return z;
	}
	public static String fullname(String name, String surname)
	{
		String fullname = name + surname;
		return fullname;
	}
}
